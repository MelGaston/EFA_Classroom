// Assignment Operator
let x = 5;

// Addition Assignment
x += 1;
console.log(x);

// Subtraction Assignment
x -= 1;
console.log(x);

// Multiplication Assignment
x *= 1;
console.log(x);

// Division Assignment
x /= 1;
console.log(x);

// Remainder (or Modulus) Assignment
x %= 1;
console.log(x);

// Exponential Assignment
x **= 1;
console.log(x);

/***************** COMPARISON OPERATORS **********************/

// Equal Operator 
console.log('3' == 3); // True

// Strict Equal Operator
console.log('3' === 3); // False - Checks more strictly, and data types do not match

// Not Equal Operator
console.log('3' != 3);

// Strict Not Equal Operator
console.log('3' !== 3);

// Greater Than Operator
console.log(3 > 2);

// Greater Than (Or Equal To) Operator
console.log(3 >= 2);

// Less Than Operator
console.log(3 < 2);

// Less Than (Or Equal To) Operator
console.log(3 <= 2);

// Or Operator
console.log(2 || 3);

// And Operator
console.log(2 && 3);